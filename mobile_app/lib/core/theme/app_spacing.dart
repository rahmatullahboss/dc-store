/// Spacing Constants for consistent UI spacing
class AppSpacing {
  AppSpacing._();

  // ═══════════════════════════════════════════════════════════════
  // BASE SPACING VALUES
  // ═══════════════════════════════════════════════════════════════

  static const double xxxs = 2.0;
  static const double xxs = 4.0;
  static const double xs = 8.0;
  static const double sm = 12.0;
  static const double md = 16.0;
  static const double lg = 20.0;
  static const double xl = 24.0;
  static const double xxl = 32.0;
  static const double xxxl = 40.0;
  static const double huge = 48.0;
  static const double massive = 64.0;

  // ═══════════════════════════════════════════════════════════════
  // SEMANTIC SPACING
  // ═══════════════════════════════════════════════════════════════

  /// Spacing between inline elements (e.g., icon and text)
  static const double inline = 8.0;

  /// Spacing between related elements in a group
  static const double related = 12.0;

  /// Spacing between unrelated elements
  static const double unrelated = 16.0;

  /// Section spacing
  static const double section = 24.0;

  /// Page margin
  static const double pageMargin = 16.0;

  /// Card padding
  static const double cardPadding = 16.0;

  /// List item spacing
  static const double listItem = 12.0;

  /// Grid spacing
  static const double gridGap = 12.0;

  // ═══════════════════════════════════════════════════════════════
  // COMPONENT SPECIFIC
  // ═══════════════════════════════════════════════════════════════

  /// Button padding horizontal
  static const double buttonPaddingH = 24.0;

  /// Button padding vertical
  static const double buttonPaddingV = 14.0;

  /// Input field padding
  static const double inputPadding = 16.0;

  /// App bar height
  static const double appBarHeight = 56.0;

  /// Bottom navigation height
  static const double bottomNavHeight = 64.0;

  /// Bottom sheet handle height
  static const double bottomSheetHandle = 4.0;
}
